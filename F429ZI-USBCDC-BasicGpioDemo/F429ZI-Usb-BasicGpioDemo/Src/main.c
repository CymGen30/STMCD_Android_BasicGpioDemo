/* USER CODE BEGIN 4 */
void treatUserCommand() {
	char c;
	c = userCommand[0];
	switch (c) {
	case 'S':		// Set Led = 0 or 1
		if (userCommand[1] == 'L') {
			if (userCommand[2] == '=') {
				if (userCommand[3] == '0') {
				    HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
					sprintf(msg, "OK\r\n");
				} else if (userCommand[3] == '1') {
				    HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);
					sprintf(msg, "OK\r\n");
				} else {
					sprintf(msg, "ERROR\r\n");
				}
				CDC_Transmit_FS(msg, strlen(msg));
				return;
			}
		}
		break;
	}
	sprintf(msg, "ERROR userCommand=%s\r\n", userCommand);
	CDC_Transmit_FS(msg, strlen(msg));
	return;
}

